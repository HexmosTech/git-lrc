import { api } from '/static/ui-connectors/api.js';
import { providers, defaultForm } from '/static/ui-connectors/providers.js';
import {
  generateFriendlyConnectorName,
  getFriendlyNamePlaceholder,
  isLikelyAutoGeneratedName,
} from '/static/ui-connectors/name-utils.js';
import { parseRoute, routePath, navigate } from '/static/ui-connectors/router.js';
import { HeaderNav } from '/static/ui-connectors/components/HeaderNav.js';
import { Breadcrumbs } from '/static/ui-connectors/components/Breadcrumbs.js';
import { HomePage } from '/static/ui-connectors/pages/HomePage.js';
import { ProfilePage } from '/static/ui-connectors/pages/ProfilePage.js';
import { ConnectorsPage } from '/static/ui-connectors/pages/ConnectorsPage.js';
import { ConnectorFormPage } from '/static/ui-connectors/pages/ConnectorFormPage.js';

const { render, useEffect, useMemo, useRef, useState, html } = window.preact;

function App() {
  const suppressHashChangeRef = useRef(false);
  const [route, setRoute] = useState(parseRoute(window.location.hash));
  const [connectors, setConnectors] = useState([]);
  const [draftOrders, setDraftOrders] = useState({ leader: [], helper: [] });
  const [activeRoleTab, setActiveRoleTab] = useState('leader');
  const [helperSettings, setHelperSettings] = useState({ helperEnabled: true, helperMode: 'concise_then_expand' });
  const [helperSettingsSaving, setHelperSettingsSaving] = useState(false);
  const [helperSettingsSaved, setHelperSettingsSaved] = useState(false);
  const [helperSettingsError, setHelperSettingsError] = useState('');
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState('');
  const [status, setStatus] = useState('');
  const [saving, setSaving] = useState(false);
  const [fetchingModels, setFetchingModels] = useState(false);
  const [modelsFetched, setModelsFetched] = useState(false);
  const [form, setForm] = useState(defaultForm());
  const [ollamaModels, setOllamaModels] = useState([]);
  const [bedrockModels, setBedrockModels] = useState([]);
  const [dynamicModels, setDynamicModels] = useState([]);
  const [apiDefaultModel, setApiDefaultModel] = useState('');
  const [session, setSession] = useState(null);
  const [reauthInProgress, setReauthInProgress] = useState(false);
  const [orgSwitching, setOrgSwitching] = useState(false);

  const selectedProvider = useMemo(() => {
    return providers.find((provider) => provider.id === form.provider_name) || providers[0];
  }, [form.provider_name]);

  useEffect(() => {
    if (!form.provider_name || form.provider_name === 'ollama' || form.provider_name === 'bedrock') {
      setDynamicModels([]);
      setApiDefaultModel('');
      return;
    }

    let cancelled = false;
    async function loadModels() {
      setFetchingModels(true);
      try {
        const { fetchProviderModels } = await import('/static/ui-connectors/api.js');
        const data = await fetchProviderModels(form.provider_name);
        if (!cancelled && data && Array.isArray(data.models)) {
          const modelIds = data.models.map(m => m.model_id);
          setDynamicModels(modelIds);
          
          const defaultModelObj = data.models.find(m => m.is_default);
          const defaultModel = defaultModelObj ? defaultModelObj.model_id : (modelIds[0] || '');
          if (defaultModelObj) {
            setApiDefaultModel(defaultModelObj.model_id);
          } else if (modelIds.length > 0) {
            setApiDefaultModel(modelIds[0]);
          }

          if (!form.selected_model || !modelIds.includes(form.selected_model)) {
            if (defaultModel) {
              setForm(prev => ({ ...prev, selected_model: defaultModel }));
            }
          }
        }
      } catch (err) {
        console.error('Failed to load dynamic models, using static fallback:', err);
      } finally {
        if (!cancelled) {
          setFetchingModels(false);
        }
      }
    }

    loadModels();
    return () => {
      cancelled = true;
    };
  }, [form.provider_name]);

  const modelOptions = useMemo(() => {
    if (form.provider_name === 'ollama' && ollamaModels.length > 0) {
      return ollamaModels;
    }
    if (form.provider_name === 'bedrock' && bedrockModels.length > 0) {
      return bedrockModels.map((model) => model.model_id);
    }
    if (dynamicModels.length > 0) {
      return dynamicModels;
    }
    return selectedProvider.models || [];
  }, [selectedProvider, form.provider_name, ollamaModels, bedrockModels, dynamicModels]);

  const [searchQuery, setSearchQuery] = useState('');
  const [isOpen, setIsOpen] = useState(false);
  const dropdownRef = useRef(null);

  const filteredModels = useMemo(() => {
    if (!searchQuery) {
      return modelOptions;
    }
    return modelOptions.filter((model) =>
      model.toLowerCase().includes(searchQuery.toLowerCase())
    );
  }, [modelOptions, searchQuery]);

  useEffect(() => {
    setSearchQuery('');
    setIsOpen(false);
  }, [form.provider_name]);

  useEffect(() => {
    if (!helperSettingsSaved) {
      return;
    }
    const timer = window.setTimeout(() => setHelperSettingsSaved(false), 2500);
    return () => window.clearTimeout(timer);
  }, [helperSettingsSaved]);

  useEffect(() => {
    const handleClickOutside = (event) => {
      if (dropdownRef.current && !dropdownRef.current.contains(event.target)) {
        setIsOpen(false);
      }
    };
    document.addEventListener('mousedown', handleClickOutside);
    return () => document.removeEventListener('mousedown', handleClickOutside);
  }, []);

  function canonicalOrderForRole(list, role) {
    return list
      .filter((item) => (item.role || 'leader') === role)
      .sort((left, right) => (left.display_order || 0) - (right.display_order || 0))
      .map((item) => String(item.id));
  }

  function buildDraftOrders(list) {
    return {
      leader: canonicalOrderForRole(list, 'leader'),
      helper: canonicalOrderForRole(list, 'helper'),
    };
  }

  function ordersEqual(left, right) {
    if (left.length !== right.length) {
      return false;
    }
    return left.every((value, index) => value === right[index]);
  }

  const hasOrderChangesByRole = useMemo(() => {
    return {
      leader: !ordersEqual(canonicalOrderForRole(connectors, 'leader'), draftOrders.leader),
      helper: !ordersEqual(canonicalOrderForRole(connectors, 'helper'), draftOrders.helper),
    };
  }, [connectors, draftOrders]);

  const hasOrderChanges = hasOrderChangesByRole[activeRoleTab];
  const hasAnyOrderChanges = hasOrderChangesByRole.leader || hasOrderChangesByRole.helper;

  const activePath = routePath(route);
  const editingConnector =
    route.name === 'edit'
      ? connectors.find((entry) => String(entry.id) === String(route.connectorID))
      : null;
  const breadcrumbConnectorLabel =
    editingConnector?.connector_name || form.connector_name || (route.connectorID ? `Connector #${route.connectorID}` : 'Edit Connector');

  const pageTitle = useMemo(() => {
    if (route.name === 'connectors') {
      return 'git-lrc Manager | AI Connectors | List';
    }

    if (route.name === 'new') {
      return 'git-lrc Manager | AI Connectors | Add Connector';
    }

    if (route.name === 'edit') {
      const label = breadcrumbConnectorLabel ? ` | ${breadcrumbConnectorLabel}` : '';
      return `git-lrc Manager | AI Connectors | Edit Connector${label}`;
    }

    if (route.name === 'profile') {
      return 'git-lrc Manager | Profile';
    }

    return 'git-lrc Manager | Home';
  }, [route.name, breadcrumbConnectorLabel]);

  async function loadSessionStatus(silent = false) {
    try {
      const nextSession = await api('/api/ui/session-status');
      setSession(nextSession);
      if (!nextSession.authenticated && !silent) {
        setStatus('Session required for connector management');
      }
      return nextSession;
    } catch (err) {
      if (!silent) {
        setError(err.message || String(err));
      }
      return null;
    }
  }

  async function handleAuthError(err) {
    if (Number(err && err.status) === 401) {
      await loadSessionStatus(true);
      setStatus('Session expired or missing. Use Re-authenticate in the header.');
      setError('Session expired or missing.');
      return true;
    }
    return false;
  }

  async function runReauthenticate() {
    setReauthInProgress(true);
    setError('');
    setStatus('Starting browser login...');
    try {
      await api('/api/ui/auth/reauth', { method: 'POST' });
      const refreshedSession = await loadSessionStatus(true);
      if (refreshedSession) {
        setSession(refreshedSession);
      }
      if (refreshedSession && refreshedSession.authenticated && refreshedSession.org_id) {
        setStatus('Re-authentication complete');
        await loadConnectors(true, refreshedSession);
      } else {
        setConnectors([]);
        setDraftOrders({ leader: [], helper: [] });
        setStatus('Re-authentication complete. Select organization context in the header.');
      }
      navigate('/connectors');
    } catch (err) {
      setError(err.message || String(err));
      setStatus('Re-authentication failed');
    } finally {
      setReauthInProgress(false);
    }
  }

  async function loadHelperSettings() {
    try {
      const data = await api('/api/ui/connectors/settings');
      setHelperSettings({
        helperEnabled: Boolean(data.helper_enabled),
        helperMode: data.helper_mode || 'concise_then_expand',
      });
    } catch (err) {
      console.error('Failed to load helper settings:', err);
    }
  }

  async function saveHelperSettings() {
    setHelperSettingsSaving(true);
    setHelperSettingsSaved(false);
    setHelperSettingsError('');
    try {
      const data = await api('/api/ui/connectors/settings', {
        method: 'PUT',
        body: JSON.stringify({
          helper_enabled: helperSettings.helperEnabled,
          helper_mode: helperSettings.helperMode,
        }),
      });
      setHelperSettings({
        helperEnabled: Boolean(data.helper_enabled),
        helperMode: data.helper_mode || 'concise_then_expand',
      });
      setHelperSettingsSaved(true);
    } catch (err) {
      if (await handleAuthError(err)) {
        return;
      }
      setHelperSettingsError(err.message || String(err));
    } finally {
      setHelperSettingsSaving(false);
    }
  }

  function setHelperSetting(field, value) {
    setHelperSettings((prev) => ({ ...prev, [field]: value }));
    setHelperSettingsSaved(false);
  }

  async function loadConnectors(force = false, sessionSnapshot = null) {
    setLoading(true);
    setError('');
    try {
      const activeSession = sessionSnapshot || session;
      if (!activeSession || !activeSession.authenticated || !activeSession.org_id) {
        setConnectors([]);
        setDraftOrders({ leader: [], helper: [] });
        setStatus(activeSession && activeSession.authenticated ? 'Select organization context to load connectors' : 'Authenticate to load connectors');
        return;
      }
      const data = await api('/api/ui/connectors');
      const sorted = [...data].sort((left, right) => (left.display_order || 0) - (right.display_order || 0));
      setConnectors(sorted);
      setDraftOrders(buildDraftOrders(sorted));
      setStatus(`Loaded ${sorted.length} connector(s)`);
      await loadHelperSettings();
    } catch (err) {
      if (await handleAuthError(err)) {
        return;
      }
      setError(err.message || String(err));
    } finally {
      setLoading(false);
    }
  }

  async function switchOrganization(nextOrgID) {
    const targetOrgID = String(nextOrgID || '').trim();
    const currentOrgID = String((session && session.org_id) || '').trim();
    if (!targetOrgID || targetOrgID === currentOrgID) {
      return;
    }

    setOrgSwitching(true);
    setError('');
    setStatus('Switching organization context...');
    try {
      await api('/api/ui/org-context', {
        method: 'PUT',
        body: JSON.stringify({ org_id: targetOrgID }),
      });

      const refreshedSession = await loadSessionStatus(true);
      if (refreshedSession) {
        setSession(refreshedSession);
      }

      if (refreshedSession && refreshedSession.authenticated && refreshedSession.org_id) {
        await loadConnectors(true, refreshedSession);
        setStatus('Organization context switched');
      } else {
        setConnectors([]);
        setDraftOrders({ leader: [], helper: [] });
        setStatus('Organization switch complete. Select organization context in the header.');
      }
    } catch (err) {
      if (await handleAuthError(err)) {
        return;
      }
      setError(err.message || String(err));
      setStatus('Failed to switch organization context');
    } finally {
      setOrgSwitching(false);
    }
  }

  useEffect(() => {
    (async () => {
      const currentSession = await loadSessionStatus(true);
      if (currentSession && currentSession.authenticated && currentSession.org_id) {
        await loadConnectors(false, currentSession);
      }
    })();
  }, []);

  useEffect(() => {
    document.title = pageTitle;
  }, [pageTitle]);

  useEffect(() => {
    const onHashChange = () => {
      if (suppressHashChangeRef.current) {
        suppressHashChangeRef.current = false;
        return;
      }

      const nextRoute = parseRoute(window.location.hash);
      const leavingConnectorsWithUnsavedOrder =
        route.name === 'connectors' &&
        hasAnyOrderChanges &&
        nextRoute.name !== 'connectors';

      if (leavingConnectorsWithUnsavedOrder) {
        const shouldLeave = window.confirm('You have unsaved priority changes. Leave without saving?');
        if (!shouldLeave) {
          suppressHashChangeRef.current = true;
          window.location.hash = routePath(route);
          return;
        }

        setDraftOrders(buildDraftOrders(connectors));
      }

      setRoute(nextRoute);
    };

    window.addEventListener('hashchange', onHashChange);
    onHashChange();
    return () => window.removeEventListener('hashchange', onHashChange);
  }, [route, hasAnyOrderChanges]);

  useEffect(() => {
    const onBeforeUnload = (event) => {
      const hasUnsavedOrderChanges = route.name === 'connectors' && hasAnyOrderChanges;
      if (!hasUnsavedOrderChanges) {
        return;
      }

      event.preventDefault();
      event.returnValue = '';
    };

    window.addEventListener('beforeunload', onBeforeUnload);
    return () => window.removeEventListener('beforeunload', onBeforeUnload);
  }, [route.name, hasAnyOrderChanges]);

  useEffect(() => {
    if (route.name === 'new') {
      resetFormState(activeRoleTab);
      setError('');
      setStatus('');
      return;
    }

    if (route.name === 'edit') {
      const connector = connectors.find((entry) => String(entry.id) === String(route.connectorID));
      if (connector) {
        setOllamaModels([]);
        setBedrockModels([]);
        setModelsFetched(false);
        setFetchingModels(false);
        setForm({
          id: String(connector.id),
          provider_name: connector.provider_name || 'gemini',
          connector_name: connector.connector_name || 'Connector',
          api_key: connector.api_key || '',
          base_url: connector.base_url || '',
          selected_model: connector.selected_model || '',
          gcp_project_id: connector.gcp_project_id || '',
          gcp_location: connector.gcp_location || '',
          aws_access_key_id: connector.aws_access_key_id || '',
          aws_region: connector.aws_region || '',
          role: connector.role || 'leader',
        });
        setStatus(`Editing connector #${connector.id}`);
      }
    }
  }, [route.name, route.connectorID, connectors, activeRoleTab]);

  function resetFormState(role) {
    setForm({ ...defaultForm(), role: role || 'leader' });
    setOllamaModels([]);
    setBedrockModels([]);
    setApiDefaultModel('');
    setModelsFetched(false);
    setFetchingModels(false);
  }

  function setProvider(providerId) {
    const provider = providers.find((entry) => entry.id === providerId) || providers[0];
    setForm((prev) => ({
      ...prev,
      provider_name: provider.id,
      connector_name: isLikelyAutoGeneratedName(prev.connector_name, providers)
        ? generateFriendlyConnectorName(provider.id, providers)
        : prev.connector_name,
      selected_model: provider.modelPresets?.length ? provider.modelPresets[0] : '',
      base_url: provider.requiresBaseURL
        ? (provider.baseURLPresets?.length ? provider.baseURLPresets[0].value : '')
        : '',
      gcp_project_id: '',
      gcp_location: '',
      aws_access_key_id: '',
      aws_region: '',
    }));
    if (provider.id !== 'ollama') {
      setOllamaModels([]);
    }
    if (provider.id !== 'bedrock') {
      setBedrockModels([]);
    }
    if (provider.id !== 'ollama' && provider.id !== 'bedrock') {
      setApiDefaultModel('');
      setModelsFetched(false);
    }
    setStatus('');
    setError('');
  }

  function setFormField(field, value) {
    setForm((prev) => ({ ...prev, [field]: value }));
  }

  function generateConnectorName() {
    const provider = providers.find((entry) => entry.id === form.provider_name) || providers[0];
    setForm((prev) => ({
      ...prev,
      connector_name: generateFriendlyConnectorName(provider.id, providers),
    }));
  }

  function editConnector(connector) {
    setForm({
      id: String(connector.id),
      provider_name: connector.provider_name || 'gemini',
      connector_name: connector.connector_name || 'Connector',
      api_key: connector.api_key || '',
      base_url: connector.base_url || '',
      selected_model: connector.selected_model || '',
      gcp_project_id: connector.gcp_project_id || '',
      gcp_location: connector.gcp_location || '',
      aws_access_key_id: connector.aws_access_key_id || '',
      aws_region: connector.aws_region || '',
      role: connector.role || 'leader',
    });
    setOllamaModels([]);
    setBedrockModels([]);
    setModelsFetched(false);
    setFetchingModels(false);
    setStatus(`Editing connector #${connector.id}`);
    navigate(`/connectors/edit/${connector.id}`);
  }

  async function fetchOllamaModels() {
    setFetchingModels(true);
    setError('');
    setStatus('');
    if (!form.base_url) {
      setError('Base URL is required for Ollama model discovery');
      setFetchingModels(false);
      return;
    }
    try {
      const response = await api('/api/ui/connectors/ollama/models', {
        method: 'POST',
        body: JSON.stringify({
          base_url: form.base_url,
          jwt_token: form.api_key || undefined,
        }),
      });
      const models = response.models || [];
      setOllamaModels(models);
      setModelsFetched(true);
      setForm((prev) => {
        if (prev.selected_model && models.includes(prev.selected_model)) {
          return prev;
        }
        return { ...prev, selected_model: '' };
      });
      setStatus(`Fetched ${models.length} Ollama model(s)`);
    } catch (err) {
      setModelsFetched(false);
      if (await handleAuthError(err)) {
        return;
      }
      setError(err.message || String(err));
    } finally {
      setFetchingModels(false);
    }
  }

  async function fetchBedrockModels() {
    setFetchingModels(true);
    setError('');
    setStatus('');
    const awsRegion = (form.aws_region || '').trim();
    if (!awsRegion) {
      setError('Region is required for Bedrock model discovery');
      setFetchingModels(false);
      return;
    }
    try {
      const response = await api('/api/ui/connectors/bedrock/models', {
        method: 'POST',
        body: JSON.stringify({
          access_key_id: (form.aws_access_key_id || '').trim(),
          secret_access_key: form.api_key || '',
          region: awsRegion,
        }),
      });
      const models = response.models || [];
      setBedrockModels(models);
      setModelsFetched(true);
      const modelIds = models.map((model) => model.model_id);
      setForm((prev) => {
        if (prev.selected_model && modelIds.includes(prev.selected_model)) {
          return prev;
        }
        return { ...prev, selected_model: '' };
      });
      if (models.length === 0) {
        setError('No foundation models found for this region. Request model access in the AWS Bedrock console first.');
      } else {
        setStatus(`Fetched ${models.length} Bedrock model(s)`);
      }
    } catch (err) {
      setModelsFetched(false);
      if (await handleAuthError(err)) {
        return;
      }
      setError(err.message || String(err));
    } finally {
      setFetchingModels(false);
    }
  }

  async function saveConnector() {
    const provider = providers.find((entry) => entry.id === form.provider_name) || providers[0];
    const connectorName = (form.connector_name || '').trim();
    const apiKey = (form.api_key || '').trim();
    const baseURL = (form.base_url || '').trim();
    const selectedModel = (form.selected_model || '').trim();
    let hasValidBaseURL = true;
    if (provider.requiresBaseURL) {
      try {
        const parsedURL = new URL(baseURL);
        hasValidBaseURL =
          (parsedURL.protocol === 'http:' || parsedURL.protocol === 'https:') &&
          Boolean(parsedURL.host);
      } catch {
        hasValidBaseURL = false;
      }
    }

    if (!connectorName) {
      setError('Connector name is required');
      setStatus('');
      return;
    }

    if (provider.id === 'ollama') {
      if (!baseURL) {
        setError('Base URL is required');
        setStatus('');
        return;
      }

      if (!hasValidBaseURL) {
        setError('Base URL must be a valid http:// or https:// URL');
        setStatus('');
        return;
      }

      if (!selectedModel) {
        setError('Please select a model');
        setStatus('');
        return;
      }
    } else if (provider.id === 'bedrock') {
      if (!(form.aws_access_key_id || '').trim()) {
        setError('AWS Access Key ID is required');
        setStatus('');
        return;
      }
      if (!apiKey) {
        setError('AWS Secret Access Key is required');
        setStatus('');
        return;
      }
      if (!(form.aws_region || '').trim()) {
        setError('Region is required');
        setStatus('');
        return;
      }
      if (!selectedModel) {
        setError('Please select a model');
        setStatus('');
        return;
      }
    } else if (!apiKey) {
      setError('API key is required');
      setStatus('');
      return;
    }

    setSaving(true);
    setError('');
    setStatus('');

    try {
      if (provider.id !== 'ollama') {
        setStatus('Validating API key...');
        const validationResponse = await api('/api/ui/connectors/validate-key', {
          method: 'POST',
          body: JSON.stringify({
            provider: provider.id,
            api_key: apiKey,
            base_url: baseURL || undefined,
            model: selectedModel || apiDefaultModel || undefined,
            gcp_project_id: (form.gcp_project_id || '').trim() || undefined,
            gcp_location: (form.gcp_location || '').trim() || undefined,
            aws_access_key_id: (form.aws_access_key_id || '').trim() || undefined,
            aws_region: (form.aws_region || '').trim() || undefined,
          }),
        });

        if (!validationResponse.valid) {
          setError(validationResponse.message || 'API key validation failed');
          setStatus('');
          return;
        }
      }

      const payload = {
        provider_name: provider.id,
        api_key: apiKey,
        connector_name: connectorName,
        base_url: baseURL || undefined,
        selected_model: selectedModel || apiDefaultModel || undefined,
        gcp_project_id: (form.gcp_project_id || '').trim() || undefined,
        gcp_location: (form.gcp_location || '').trim() || undefined,
        aws_access_key_id: (form.aws_access_key_id || '').trim() || undefined,
        aws_region: (form.aws_region || '').trim() || undefined,
        display_order: 0,
        role: form.role || 'leader',
      };

      if (form.id) {
        await api(`/api/ui/connectors/${form.id}`, {
          method: 'PUT',
          body: JSON.stringify(payload),
        });
        setStatus('Connector updated');
      } else {
        await api('/api/ui/connectors', {
          method: 'POST',
          body: JSON.stringify(payload),
        });
        setStatus('Connector created');
      }
      resetFormState();
      await loadConnectors(true);
      navigate('/connectors');
    } catch (err) {
      if (await handleAuthError(err)) {
        return;
      }
      setError(err.message || String(err));
    } finally {
      setSaving(false);
    }
  }

  async function deleteConnector(connectorId) {
    if (!confirm('Delete this connector?')) {
      return;
    }
    setError('');
    setStatus('');
    try {
      await api(`/api/ui/connectors/${connectorId}`, { method: 'DELETE' });
      setStatus(`Connector #${connectorId} deleted`);
      await loadConnectors(true);
    } catch (err) {
      if (await handleAuthError(err)) {
        return;
      }
      setError(err.message || String(err));
    }
  }

  function move(id, direction) {
    setDraftOrders((prev) => {
      const roleList = prev[activeRoleTab];
      const index = roleList.findIndex((value) => value === id);
      if (index < 0) return prev;
      const target = direction === 'up' ? index - 1 : index + 1;
      if (target < 0 || target >= roleList.length) return prev;
      const copy = [...roleList];
      [copy[index], copy[target]] = [copy[target], copy[index]];
      return { ...prev, [activeRoleTab]: copy };
    });
  }

  function reorderByDrag(sourceID, targetID) {
    if (!sourceID || !targetID || sourceID === targetID) {
      return;
    }

    setDraftOrders((prev) => {
      const roleList = prev[activeRoleTab];
      const sourceIndex = roleList.findIndex((value) => value === sourceID);
      const targetIndex = roleList.findIndex((value) => value === targetID);
      if (sourceIndex < 0 || targetIndex < 0 || sourceIndex === targetIndex) {
        return prev;
      }

      const copy = [...roleList];
      const [sourceValue] = copy.splice(sourceIndex, 1);
      copy.splice(targetIndex, 0, sourceValue);
      return { ...prev, [activeRoleTab]: copy };
    });
  }

  async function saveOrder() {
    setError('');
    setStatus('');
    try {
      const updates = draftOrders[activeRoleTab].map((id, idx) => ({ id, display_order: idx + 1 }));
      await api('/api/ui/connectors/reorder', {
        method: 'PUT',
        body: JSON.stringify(updates),
      });
      setStatus('Priority order saved');
      await loadConnectors(true);
    } catch (err) {
      if (await handleAuthError(err)) {
        return;
      }
      setError(err.message || String(err));
    }
  }

  const orderedConnectors = draftOrders[activeRoleTab]
    .map((id) => connectors.find((entry) => String(entry.id) === id))
    .filter(Boolean);

  const roleCounts = useMemo(() => {
    return {
      leader: connectors.filter((entry) => (entry.role || 'leader') === 'leader').length,
      helper: connectors.filter((entry) => (entry.role || 'leader') === 'helper').length,
    };
  }, [connectors]);

  const saveDisabled = useMemo(() => {
    const provider = providers.find((entry) => entry.id === form.provider_name) || providers[0];
    const connectorName = (form.connector_name || '').trim();
    const apiKey = (form.api_key || '').trim();
    const baseURL = (form.base_url || '').trim();
    const selectedModel = (form.selected_model || '').trim();

    if (!connectorName) {
      return true;
    }

    if (provider.id === 'ollama') {
      return !baseURL || !selectedModel;
    }

    if (provider.id === 'gemini-enterprise') {
      const gcpProjectID = (form.gcp_project_id || '').trim();
      const gcpLocation = (form.gcp_location || '').trim();
      return !apiKey || !gcpProjectID || !gcpLocation;
    }

    if (provider.id === 'bedrock') {
      const awsAccessKeyID = (form.aws_access_key_id || '').trim();
      const awsRegion = (form.aws_region || '').trim();
      return !apiKey || !awsAccessKeyID || !awsRegion || !selectedModel;
    }

    if (!apiKey) {
      return true;
    }

    if ((provider.id === 'openrouter' || provider.id === 'atlas') && !selectedModel) {
      return true;
    }

    return false;
  }, [form]);

  function renderPage() {
    if (!session || !session.authenticated) {
      const sessionCard = html`
        <div class="card session-banner">
          <h2>Session Required</h2>
          <p class="muted">${session && session.message ? session.message : 'Sign in to manage connectors.'}</p>
          <div class="row">
            <button onClick=${runReauthenticate} disabled=${reauthInProgress}>
              ${reauthInProgress ? 'Reauthenticating...' : 'Re-authenticate'}
            </button>
          </div>
        </div>
      `;

      if (route.name === 'home') {
        return html`${sessionCard}<${HomePage} />`;
      }

      return sessionCard;
    }

    if (route.name === 'connectors') {
      return html`<${ConnectorsPage}
        connectors=${connectors}
        loading=${loading}
        orderedConnectors=${orderedConnectors}
        hasOrderChanges=${hasOrderChanges}
        activeRoleTab=${activeRoleTab}
        onSwitchRoleTab=${setActiveRoleTab}
        roleCounts=${roleCounts}
        helperSettings=${helperSettings}
        helperSettingsSaving=${helperSettingsSaving}
        helperSettingsSaved=${helperSettingsSaved}
        helperSettingsError=${helperSettingsError}
        onHelperSettingChange=${setHelperSetting}
        onSaveHelperSettings=${saveHelperSettings}
        onRefresh=${loadConnectors}
        onSaveOrder=${saveOrder}
        onMove=${move}
        onDragReorder=${reorderByDrag}
        onEdit=${editConnector}
        onDelete=${deleteConnector}
        onAdd=${() => navigate('/connectors/new')}
      />`;
    }

    if (route.name === 'new' || route.name === 'edit') {
      return html`<${ConnectorFormPage}
        title=${form.id ? 'Edit Connector' : 'Add Connector'}
        form=${form}
        providers=${providers}
        selectedProvider=${selectedProvider}
        modelOptions=${modelOptions}
        filteredModels=${filteredModels}
        searchQuery=${searchQuery}
        setSearchQuery=${setSearchQuery}
        isOpen=${isOpen}
        setIsOpen=${setIsOpen}
        dropdownRef=${dropdownRef}
        fetchingModels=${fetchingModels}
        modelsFetched=${modelsFetched}
        saving=${saving}
        saveDisabled=${saveDisabled}
        status=${status}
        error=${error}
        onProviderChange=${setProvider}
        onFieldChange=${setFormField}
        onFetchOllamaModels=${fetchOllamaModels}
        onFetchBedrockModels=${fetchBedrockModels}
        onSave=${saveConnector}
        onGenerateName=${generateConnectorName}
        onCancel=${() => {
          resetFormState();
          navigate('/connectors');
        }}
        connectorNamePlaceholder=${getFriendlyNamePlaceholder(form.provider_name, providers)}
        apiDefaultModel=${apiDefaultModel}
      />`;
    }

    if (route.name === 'profile') {
      return html`<${ProfilePage}
        session=${session}
        onReauthenticate=${runReauthenticate}
        reauthInProgress=${reauthInProgress}
      />`;
    }

    return html`<${HomePage} />`;
  }

  return html`
    <div class="wrap ui-shell">
      <${HeaderNav}
        activePath=${activePath}
        session=${session}
        reauthInProgress=${reauthInProgress}
        orgSwitching=${orgSwitching}
        onReauthenticate=${runReauthenticate}
        onSwitchOrg=${switchOrganization}
      />
      <${Breadcrumbs} route=${route} connectorName=${breadcrumbConnectorLabel} />
      ${error ? html`<div class="err-banner">${error}</div>` : ''}
      ${renderPage()}
    </div>
  `;
}

render(html`<${App} />`, document.getElementById('app'));
